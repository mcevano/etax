                           <b>When and Where to File and Pay</b>
                           <br />
                           <p>The return shall be filed and the tax paid within twenty-five (25) days after the end of each taxable quarter. Provided, however, that with respect to non-large and large taxpayers who shall file through the Electronic Filing and Payment System (EFPS), the deadline for electronically filing the return and paying the taxes due thereon shall be in accordance with the provisions of existing applicable revenue issuances.</p>
                           <p>The return shall be filed and the tax paid with the Authorized Agent Bank (AAB) of the Revenue District Office (RDO) where the taxpayer is required to register/conducting business.  In places where there are no AABs, the return shall be filed and the tax paid with the Revenue Collection Officer (RCO) of the RDO where the taxpayer is required to register/conducting business, who will issue an Electronic Revenue Official Receipt (eROR) therefor.</p>
                           <p>When the return is filed with an AAB, taxpayer must accomplish and submit BIR-prescribed deposit slip, which the bank teller shall machine validate as evidence that payment was received by the AAB. The AAB receiving the tax return shall stamp mark the word “Received” on the return and also machine validate the return as proof of filing the return and payment of the tax by the taxpayer, respectively. The machine validation shall reflect the date of payment, amount paid and transactions code, the name of the bank, branch code, teller’s code and teller’s initial. Bank debit memo number and date should be indicated in the return for taxpayers paying under the bank debit system.</p>
                           <p>Payments may also be made thru the epayment channels of AABs thru either their online facility, credit/debit/prepaid cards, and mobile payments.</p>
                           <p>A taxpayer may file a separate return for the head office and for each branch or place of business/office or a consolidated return for the head office and all the branches/offices. In the case of large taxpayers only one consolidated return is required.</p>
                    
                      </div>
