                      <div class="col">
                          <b>Who Shall File</b>

                            <ol class="indent">
                                This return shall be filed in triplicate by the following:
                            <li>Franchise grantees sending overseas dispatch, messages  or  conversations from the Philippines, except on services involving the following:</li>
                                <ol type="a" class="indent">
                                    <li>Government of the Philippines - for messages transmitted by the Government of the Republic of the Philippines or any of its political subdivisions and  instrumentalities;</li>
                                    <li>Diplomatic services - for messages transmitted by  any  embassy and consular offices of a foreign government;</li>
                                    <li>International organizations - for messages transmitted by a public international organization or any of its agencies based in the Philippines enjoying privileges and immunities pursuant to an international agreement; and</li>
                                    <li>News Services - for messages from any newspaper, press association, radio or television newspaper, broadcasting agency,   or newsticker services to any other newspaper, press association, radio or television newspaper broadcasting agency or newsticker services or to bonafide correspondents, which messages deal exclusively with the collection of news items for, or the dissemination of news items through, public press, radio or television broadcasting or a newsticker service furnishing a  general news service similar to that of the public  press.</li>
                                </ol>
                                <li>Proprietors, lessees or operators of cockpits, cabarets, night or  day clubs, boxing exhibitions, professional basketball games, jai-alai and racetracks.</li>
                                <li>Provided that cooperatives shall be exempt from the three percent (3%) gross receipts tax.</li>
                                <br />
                            </ol>
                            <b>When and Where to  File</b>
                            <p>The return shall be filed within twenty (20) days after the end of each quarter, provided, however, that with respect to taxpayers enrolled with the Electronic Filing and Payment System (EFPS), the deadline for e-filing and e-paying the tax due thereon shall be five (5) days later than the deadline set above. Any person retiring from a business subject to percentage taxes shall notify the nearest Revenue District Office, file his return and pay the tax due thereon within twenty (20) days after closing his  business.</p>
                            <p>The return shall be filed with any Authorized Agent Bank (AAB) within the territorial jurisdiction of the Revenue District Office where the taxpayer   is required to register/conducting business. In places where there are no  AABs, the return shall be filed with the Revenue Collection Officer or duly Authorized City or Municipal Treasurer within the Revenue District Office where the taxpayer is required to register/conducting  business.</p>
                            <p>A taxpayer may, at his option, file a separate return for the head office and for each branch or place of business or a consolidated return for the head office and all the branches or place of business except in the case of large taxpayers where only one consolidated return is  required.</p>
                            <br />
                            <b>When and Where to Pay</b>
                            <p>
                                Upon filing this return, the total amount payable shall be paid to the Authorized Agent Bank (AAB) where the return is filed. In places where there are no AABs, payment shall be made directly to the  Revenue  Collection Officer or duly Authorized City or Municipal Treasurer who shall
                            issue a Revenue Official Receipt (BIR Form No. 2524)   therefor.
                            </p>
                            <p>Where the return is filed with an AAB, the lower portion of the return must be properly machine-validated and stamped by the Authorized Agent Bank to serve as the receipt of payment. The machine validation shall reflect the date of payment, amount  paid  and  transaction  code, and the stamped mark shall show the name of the bank, branch code, teller's code and teller's initial. The AAB shall also issue an official receipt or bank debit advice or credit document, whichever is applicable, as additional proof of payment.</p>
                        
                      </div>
                      <div class="col">
                          <b>Basis of Tax</b>
                            <p>For the purpose of the amusement tax, the term “gross receipts” embraces all the receipts of the proprietor, lessee or operator of the  amusement place. Said gross receipts also include income from television, radio and motion picture rights, if any.</p>
                            <br />
                            <b>Penalties</b>
                            <p></p>
                            <ol class="indent">
                                There shall be imposed and collected as part of  the  tax:</p>
                            <li>A surcharge of twenty five percent (25%) for each of the following violations:</li>
                                <ol type="a" class="indent">
                                    <li>Failure to file any return and pay the amount of tax or installment due on or before the due date;</li>
                                    <li>Unless otherwise authorized by the Commissioner, filing a return with a person or office other than those with whom it is required   to be filed;</li>
                                    <li>Failure to pay the full or part of the amount of tax shown on the return, or the full amount of tax due for which no  return  is required to be filed on or before the due  date;</li>
                                    <li>Failure to pay the deficiency tax within the time prescribed for its payment in the notice of assessment.</li>
                                </ol>
                                <li>A surcharge of fifty percent (50%) of the tax or of the deficiency tax,    in case any payment has been made on the basis of such return before  the discovery of the falsity or fraud, for each of the  following  violations:</li>
                                <ol type="a" class="indent">
                                    <li>Willful neglect to file the return within the period prescribed by  the Code or by rules and regulations; or</li>
                                    <li>In case a false or fraudulent return is willfully made.</li>
                                </ol>
                                <li>Interest at the rate of twenty percent (20%) per annum, or such higher rate as may be prescribed by rules and regulations, on any unpaid  amount of tax from the date prescribed  for the payment until the  amount is fully paid.</li>
                                <li>Compromise penalty.</li>
                                <br />
                            </ol>
