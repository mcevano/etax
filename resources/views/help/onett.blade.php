                    <ul>
                        <li>Box No. 1 refers to the transaction period and not the date of filing
                    the return.
                    </li>
                        <li>TIN = Taxpayer Identification Number</li>
                        <li>The last 3 digits of the 12-digit TIN refers to the branch code.
                    </li>
                        <li>Onerous transfer means transfer for value</li>
                        <li>If box Nos. 17 and 18 are applicable, taxpayer should file an
