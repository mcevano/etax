                    to be filed on or before the due date;</li>
                            <li>Failure to pay the deficiency tax within the time prescribed for its
                    payment in the Notice of Assessment</li>


                        </ol>
                        <li>A surcharge of fifty percent (50%) of the tax or of the deficiency tax, in
                    case any payment has been made on the basis of such return before
                    discovery of the falsity or fraud, for each of the following violations:</li>
                        <ol class="indent" type="a">
                            <li>Willful neglect to file the return within the period prescribed by the
                    Code or by rules and regulations; or</li>
                            <li>In case a false or fraudulent return is willfully made.</li>
                        </ol>
                        <li>Interest at the rate of twenty percent (20%) per annum, or such
                    higher rate as may be prescribed by rules and regulations, on any unpaid
                    amount of tax, from the date prescribed for payment until the amount is
                    fully paid.</li>
                        <li>Compromise penalty.
                    </li>
                    </ol>
                    <dt>Attachments Required:</dt>
                    <ol>
                        <li>Copy of the Notarized Deed of Sale or Exchange;</li>
                        <li>Owner’s Copy for presentation purposes only
                          together with the photocopy thereof for
                          authentication or Certified True Copy of
                          Transfer Certificate of Title (TCT),
                          Condominium Certificate of Title (CCT),
                           Original Certificate of Title (OCT);</li>
                        <li>Certified True Copy of the latest tax declaration on lot and/or
                    improvement;</li>
                        <li>If what is sold is lot only, a certification from the Assessor’s Office that
                    there is no existing improvement on the property or that the improvement
                    is in the name of another;
                    </li>
                        <li>Copy of BIR Ruling for tax exemption confirmed by the BIR, if
                    applicable;</li>
                        <li>Duly approved Tax Debit Memo, if applicable;</li>
                        <li>For amended return, proof of tax payment and the previously filed return.</li>
                        <li>"Sworn Declaration of Intent" as prescribed in Revenue Regulations 13-
                    99, if the transaction is tax-exempt because the taxpayer falls under
                    boxes 17 & 18. (See Form 1706)</li>
                    </ol>
                    <p>These requirements must be submitted upon field or office audit of the
                    tax case before the Tax Clearance Certificate/Certificate Authorizing
                    Registration can be released to the taxpayer.</p>
