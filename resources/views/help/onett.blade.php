              <h4 class="font-weight-bold">BIR Form No. 2000OT <br> Guidelines and Instructions</h4>
              <hr>
              <h5 class="text-center font-weight-bold">
                BIR FORM 2000-OT - DOCUMENTARY STAMP TAX DECLARATION/RETURN(ONE-TIME-TRANSACTIONS)<br>
                GUIDELINES AND INSTRUCTIONS
              </h5>
              <div class="row font-sm text-justify">
                  <div class="col">
                    <b>Who Shall File</b>
                    <p>This return shall be filed in triplicate by the following person making, signing, issuing, accepting or transferring the document or facility evidencing transaction:</p>
                    <ol class="indent">
                        <li>Every natural or juridical person, resident or non-resident, for sale, barter, exchange or other onerous disposition of shares of stock in a domestic corporation, classified as <ins>capital asset</ins> , not traded in the local stock exchange;</li>
                        <li>Every withholding agent/buyer/seller on the sale, transfer or exchange of real property classified as <ins>capital asset</ins> . The “sale” includes pacto de retro sale and other forms of conditional sale; and</li>
                        <li>Every withholding agent/buyer/seller on the sale, transfer or exchange of real property classified as <ins>ordinary asset</ins>.</li>
                    </ol>
                    <p>Whenever one party to the taxable document enjoys exemption from the tax herein imposed, the other party thereto who is not exempt shall be the one directly liable for the tax.</p>
                    <br />
                    <b>When and Where to File and Pay</b>
                    <p>The return shall be filed and the tax paid within five (5) days after the close of the month when the taxable document was made, signed, issued, accepted or transferred.</p>
                    <p>The return shall be filed with and the tax paid to the Authorized Agent Bank (AAB) within the territorial jurisdiction of Revenue District Office (RDO) where the seller/transferor is required to be registered or where the property is located in case of sale of real property. In places where there are no AAB’s, the return shall be filed directly with and tax paid to the Revenue Collect ion Officer (RCO) or duly Authorized City or Municipal Treasurer within the RDO where the seller/transferor is required to be registered or where the property is located in case of real property.</p>
                    <p>Where the return is filed with an AAB, taxpayer must accomplish and submit BIR-prescribed deposit slip which the bank teller shall machine validate as evidence that the BIR tax payment is deposited to the account of the Bureau of Treasury. The AAB receiving the tax return/payment form shall also machine validat e and stamp mark the word “Received” on the return/payment form as proof of filing the return/payment form and payment of the tax by the taxpayer. The machine validation shall reflect the date of payment, amount paid and transactions code, the name of the bank, branch code, teller’s code and teller’s initial. Bank debit advice/memo shall still be issued to taxpayers paying under the bank debit system.</p>
                    <br />
                    <b>Taxable Base</b>
                    <ol class="indent" type="A">
                        <li>For Transfer of Shares of Stocks:</li>
                        <div class="indent">
                            <p>For <strong>Shares of Stocks With Par Value</strong>, the documentary stamp tax shall be based on the par value of shares being sold or transferred; and</p>
                            <p>For <strong>Shares of Stocks Without Par Value</strong>, the documentary stamp tax shall be based on documentary stamp tax paid upon the original issue of the stock.</p>
                        </div>
                        <li>For Transfer of Real Property (Capital and Ordinary assets):</li>
                        <ol class="indent">
                            <p>The Documentary Stamp Tax shall be based on whichever is higher of the following:</p>
                            <li>The fair market value as determined by the Commissioner (zonal value);</li>
                            <li>The fair market value as shown in the Schedule of Values of
                    the Provincial and City Assessors; or
                            </li>
                            <li>The selling price of the property or fair market value of the property received in an exchange transaction.</li>
                            <p>
                                Provided, that when the sale is thru mortgage foreclosure sale
                    or when one of the contracting parties is the Government, the tax herein imposed shall be based on the actual consideration.
                            </p>
                        </ol>
                    </ol>
                    <p>
                        In case of sale of real property paid under installment payment or deferred payment basis, the payment of documentary stamp tax (DST) shall accrue upon the execution of the Deed of Absolute Sale but the basis for the imposition
                    </p>
                  </div>
                  <div class="col">
                      thereof shall be the gross selling price or fair market value of the property, whichever is higher, at the time of the execution of the Contract to Sell.
                     
                     <b>Effect of Failure to Stamp Taxable Document</b>

                     <p>An    instrument,    document   or   paper which is  required   by    law  to   be stamped and which has been signed, issued, accepted or transferred without being duly stamped,  shall not  be recorded,  nor  shall  it or any copy thereof or any record of transfer of the same be admitted or used in evidence in any court until the requisite stamp or stamps shall have been affixed thereto and cancelled.</p>

                     <p>No notary public or other officer authorized to administer oaths shall add his jurat or acknowledgment to any document subject to documentary stamp tax unless the proper documentary stamps are affixed thereto and cancelled.</p>
                     
                     <b>Penalties</b>
                    
                      
                    <p> There shall be imposed and collected as part of the tax:</p>
                     <ol class="indent">
                         <li>A surcharge of twenty five percent (25%) for each of the following violations:</li>
                         <ol class="indent" type="a">
                             <li>Failure to file any return and pay the amount of tax or installment due on or before the due date;</li>
                             <li>Unless otherwise authorized by the Commissioner, filing a return with a
                    person or office other than those with whom it is required to be filed;
                             </li>
                             <li>Failure to pay the full or part of the amount of tax shown on the return, or the full amount of tax due for which no return is required to be filed on or before the due date;</li>
                             <li>Failure to pay the deficiency tax within the time prescribed for its payment in the notice of assessment.</li>
                         </ol>
                         <li>A surcharge of fifty percent (50%) of the tax or of the deficiency tax, in case any payment has been made on the basis of such return before the discovery of the falsity or fraud, for each of the following violations:</li>
                         <ol class="indent" type="a">
                             <li>Willful neglect to file the return within the period prescribed by the Code or by rules and regulations; or</li>
                             <li>In case a false or fraudulent return is wilfully made.</li>
                         </ol>
                         <li>Interest at the rate of twenty percent (20%) per annum, or such high er rate as may be prescribed by rules and regulations, on any unpaid amount of tax, from the date prescribed for the payment until the amount is fully paid.</li>
                         <li>Compromise penalty.</li>
                     </ol>
