              <h5 class="text-center font-weight-bold">
                BIR FORM 1606 -Withholding Tax Remittance Return <br>
                [For Onerous Transfer of Real Property Other Than Capital Asset - Including Taxable and Exempt]<br>
                Guidelines and Instructions
              </h5>
              <div class="row font-sm text-justify">
                  <div class="col">
                      <dt>Who Shall File</dt>
                      <p>
                          This return shall be filed in triplicate by every Withholding
                      Agent/Buyer on the sale, transfer or exchange of real property classified as
                      ordinary asset. One return should be filed for every property (including
                      portion of a property) purchased.
                      </p>
                      <p>The term <strong>“Ordinary Asset”</strong> shall mean property held by the taxpayer
                      other than capital asset including stock in trade, or other property of a kind
                      which would properly be included in the inventory if on hand at the close of
                      the taxable year, or property held primarily for sale to customers in the
                      ordinary course of trade or business, or property used in trade or business of a
                      character which is subject to the allowance for depreciation, or real property
                      used in trade or business.
                      </p>
                      <dt>When and Where to File</dt>
                      <p>The return shall be filed on or before the tenth (10th) day following the
                      end of the month in which the transaction occurred. Provided, however, that
                      taxes withheld in December shall be filed on or before January 25 of the
                      following year. Provided, that large taxpayers as determined by the
                      Commissioner shall file on or before the 25th day of the following month.</p>
                      <p>The return shall be filed with any Authorized Agent Bank (AAB) of
                      the Revenue District Office (RDO) having jurisdiction over the place where
                      the property being transferred is located. In places where there are no
                      Authorized Agent Banks (AABs), the return shall be filed with the Revenue
                      Collection Officer or duly Authorized City or Municipal Treasurer of the
                      Revenue District Office having jurisdiction over the place where the property
                      being transferred is located.
                      </p>
                      <dt>When and Where to Remit
                      </dt>
                      <p>Upon filing of this return, the total amount to be remitted shall be paid
                      to the Authorized Agent Bank (AAB) where the return is filed. In places
                      where there are no Authorized Agent Banks (AABs), payment shall be made
                      directly to Revenue Collection Officer or the duly Authorized City or
                      Municipal Treasurer who shall issue Revenue Official Receipt (BIR Form No.
                      2524) therefor.</p>
                      <p>Where the return is filed with an AAB, the lower portion of the return
                      must be properly machine-validated and stamped by the Authorized Agent
                      Bank to serve as the receipt of payment. The machine validation shall
                      reflect the date of payment, amount paid and transaction code and the stamp
                      mark shall show the name of the bank, branch code, teller’s code and
                      teller’s initial. The AAB shall also issue an official receipt or bank debit
                      advice or credit document, whichever is applicable, as additional proof of
                      payment.
                      </p>
                  </div>
                  <div class="col">
                    <dt>Penalties</dt>
                        <p>There shall be imposed and collected as part of the tax:</p>
                        <ol>
                        <li>A surcharge of twenty five percent (25%) for each of the following
                        violations:
                        </li>
                        <ol class="indent" type="a">
                        <li>Failure to file any return and pay the amount of tax or installment
                        due on or before the due date;</li>
                        <li>Unless otherwise authorized by the Commissioner, filing a return
                        with a person or office other than those with whom it is required to
                        be filed;
                        </li>
                        <li>Failure to pay the full or part of the amount of tax shown on the
                        return, or the full amount of tax due for which no return is
                        required to be filed on or before the due date;</li>
                        <li>Failure to pay the deficiency tax within the time prescribed for its
                        payment in the notice of assessment.</li>
                        </ol>
                        <li>A surcharge of fifty percent (50%) of the tax or of the deficiency tax, in
                        case any payment has been made on the basis of such return before the
                        discovery of the falsity or fraud, for each of the following violations:
                        </li>
                        <ol class="indent" type="a">
                        <li>Willful neglect to file the return within the period prescribed
                        by the Code or by rules and regulations; or</li>
                        <li>In case a false or fraudulent return is willfully made.</li>
                        </ol>
                        <li>Interest at the rate of twenty percent (20%) per annum, or such higher
                        rate as may be prescribed by rules and regulations, on any unpaid
                        amount of tax, from the date prescribed for the payment until the
                        amount is fully paid.</li>
                           
                        <li>Compromise penalty</li>
                         </ol>
