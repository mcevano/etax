                    (i.e., those engaged in the trade/business or profession who are also earning compensation income) in accordance with Sec. 51 of the Code, as amended. 
                    The annual income tax return summarizes all the transactions covering the calendar year of the taxpayer.
                    </p>

                    <p>
                        This return shall be filed by the following individuals regardless of amount of 
                     gross income:
                    </p>
                    
                    <ol>
                        <li>A resident citizen engaged in trade, business, or practice of profession within and without the Philippines.</li>

                        <li>A resident alien, non-resident citizen or non-resident alien individual engaged in trade, business or practice of profession within the Philippines.</li>

                        <li>A trustee of a trust, guardian of a minor, executor/administrator of an estate, or any person acting in any fiduciary capacity for any person, where such trust, estate, minor, or person is engaged in trade or business.</li>

                        <li>An individual engaged in trade or business or in the exercise of their profession and receiving compensation income as well.</li>
                    </ol>
                    <p>
                        All individuals, estates and trusts above required under the law 
                    and existing issuances to file this return should also fill up Part IX hereof.
                     Said individuals, estates and trusts shall declare such income subject to final 
                    tax and those exclusions from gross income under Section 32(B) of the Tax Code,
                     as amended.
                    </p>
                    <p>
                        Married individuals shall file a return for the taxable year to include 
                    the income of both spouses, computing separately their 
                    individual income tax based on their respective total taxable income. 
                    Where it is impracticable for the spouses to file one return, each spouse may 
                    file a separate return of income. If any income cannot be definitely attributed 
                    to or identified as income exclusively earned or realized by either of the spouses,
                     the same shall be divided equally between the spouses for the purpose of determining their respective taxable income.
                    </p>

                    <p>
                        The income of unmarried minors derived from property received from a living parent shall be included in the return of the parent except (1) when the donor’s tax has been paid on such property, or (2) when the transfer of such property is exempt from donor’s tax.
                    </p>
                    <p>
                        If the taxpayer is unable to make his own return, the return may be made by his duly authorized agent or representative or by the guardian or other person charged with the care of his person or property, the principal and his representative or guardian assuming the responsibility of making the return and incurring penalties provided for erroneous, false or fraudulent returns.
                    </p>
