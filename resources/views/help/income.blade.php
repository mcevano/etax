                    (AAB) located within the territorial jurisdiction of the Revenue District Office
                    where the taxpayer is required to register/where the taxpayer has his legal
                    residence or place of business in the Philippines. In places where there are no
                    AABs, the returns shall be filed with the Revenue Collection Officer or duly
                    Authorized City or Municipal Treasurer of the Revenue District Office where
                    the taxpayer is required to register/where the taxpayer has his legal residence or
                    place of business in the Philippines. In case taxpayer has no legal residence or
                    place of business in the Philippines, the return shall be filed with the Office of the
                    Commissioner or Revenue District Office No. 39, South Quezon City.
                    </p>

