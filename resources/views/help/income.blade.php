                    taxable year
                        </p>
                    </dd>
                    <dd>
                        <p>
                            2nd qtr On or before August 15 of the
                    current taxable year
                        </p>
                    </dd>
                    <dd>
                        <p>
                            3rd qtr On or before November 15 of the
                    current taxable year
                        </p>
                    </dd>

