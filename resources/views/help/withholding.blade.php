		      	  <h4 class="font-weight-bold">Guidelines and Instructions for BIR Form No. 0619-E (January 2018) <br/> Monthly Remittance Form of Creditable Income Taxes Withheld (Expanded) </h4>
                  <hr>
                  <div class="row font-sm text-justify">
                      <div class="col">
							<dt>Who Shall File</dt>
							<p>This withholding tax remittance form shall be filed in triplicate by every withholding agent (WA)/payor required to deduct and withhold taxes on income payments subject to Expanded/Creditable Withholding Taxes. </p>
							<p>If the person required to withhold and remit the tax is a corporation, the form shall be made in the name of the corporation and shall be signed and verified by the president, vice-president, or any authorized officer.</p>
							<p>If the Government of the Philippines or any of its agencies, political subdivisions or instrumentalities or a government-owned or controlled corporation, is the withholding agent/payor, the form shall be accomplished and signed by the officer or employee having control of the disbursement of income payment or other officer or employee appropriately designated for the purpose.</p>
							<p>With respect to a fiduciary, the form shall be made in the name of the individual, estate or trust for which such fiduciary acts and shall be signed and verified by such fiduciary. In case of two or more joint fiduciaries, the form shall be signed and verified by one of such fiduciaries.</p>
							<p>Authorized Representative and Accredited Tax Agent filing, in behalf of the taxpayer, shall also use this form to remit the creditable taxes withheld.</p>

							<dt>When and Where to File and Remit</dt>
							<p>The withholding tax remittance form shall be filed and the tax remitted on or before the tenth (10th) day following the month in which withholding was made. This shall be filed for the first 2 months of each calendar quarter. </p>
							<p>Provided, however, that with respect to non-large and large taxpayers who shall file through the Electronic Filing and Payment System (eFPS), the deadline for electronically filing the form and remitting the taxes due thereon shall be in accordance with the provisions of existing applicable revenue issuances.</p>
							<p>The form shall be filed and the tax remitted with the Authorized Agent Bank (AAB) of the Revenue District Office (RDO) having jurisdiction over the WA’s place of business/office. In places where there are no AABs, the form shall be filed and the tax remitted with the Revenue Collection Officer (RCO) of the RDO having jurisdiction over the WA’s place of business/office, who will issue an Electronic Revenue Official Receipt (eROR) therefor.</p>
							<p>When the form is filed with an AAB, taxpayer must accomplish and submit BIR-prescribed deposit slip, which the bank teller shall machine validate as evidence that remittance was received by the AAB. The AAB receiving the tax form shall stamp mark the word “Received” on the form and also machine validate the form as proof of filing and remittance of the tax by the taxpayer. The machine validation shall reflect the date of remittance, amount remitted and transactions code, the name of the bank, branch code, teller’s code and teller’s initial. Bank debit memo number and date should be indicated in the form for taxpayers remitting under the bank debit system.</p>
							<p>A taxpayer may file a separate form for the head office and for each branch or place of business/office or a consolidated form for the head office and all the branches/offices. In the case of large taxpayers only one consolidated form is required.</p>

							<dt>Penalties</dt>
							<p>There shall be imposed and collected as part of the tax:</p>
							<ol>
							    <li>A surcharge of twenty-five percent (25%) for the following violations:</li>
							    <ol class="indent" type="a">
							        <li>Failure to file any form and pay/remit the amount of tax or installment due on or before the due date; or</li>
							        <li>Filing a form with a person or office other than those with whom it is required to be filed, unless otherwise authorized by the Commissioner; or</li>
							        <li>Failure to pay/remit the full or part of the amount of tax shown on the form, or the full amount of tax due for which no form is required to be filed on or before the due date; or</li>
							        <li>Failure to pay/remit the deficiency tax within the time prescribed for its payment/remittance in the notice of assessment.</li>
							    </ol>
							    <li>A surcharge of fifty percent (50%) of the tax or of the deficiency tax, in case any payment/remittance has been made before the discovery of the falsity or fraud, for each of the following violations:</li>
							       <ol class="indent" type="a">
							            <li>Willful neglect to file the form within the period prescribed by the National Internal Revenue Code (NIRC), as amended, or by rules and regulations; or</li>
							            <li>A false or fraudulent form is willfully made.</li>
							       </ol>
							</ol>
                      </div>
                      <div class="col">
                      		<ol start="3">
							    <li>Interest at the rate of double the legal interest rate for loans or forbearance of any money in the absence of an express stipulation as set by the Bangko Sentral ng Pilipinas from the date prescribed for payment/remittance until the amount is fully paid/remitted: Provided, That in no case shall the deficiency and the delinquency interest prescribed under Section 249 Subsections (B) and (C) of the NIRC, as amended, be imposed simultaneously.</li>
							    <li>Compromise penalty as provided under applicable rules and regulations.</li>
							</ol>

							<dt>Violation of Withholding Tax Provisions</dt>
							<p>Any person required to withhold, account for, and remit any tax imposed by the National Internal Revenue Code (NIRC), as amended, or who willfully fails to withhold such tax, or account for and remit such tax, or aids or abets in any manner to evade any such tax or the payment thereof, shall, in addition to other penalties provided for under the Law, be liable upon conviction to a penalty equal to the total amount of the tax not withheld, or not accounted for and remitted.</p>
							<p>Any person required under the NIRC, as amended, or by rules and regulations promulgated thereunder to pay any tax, make a form, keep any record, or supply correct and accurate information, who willfully fails to pay such tax, make such form, keep such record, or supply such correct and accurate information, or withhold or remit taxes withheld, or refund excess taxes withheld on compensation, at the time or times required by law or rules and regulations shall, in addition to the other penalties provided by law, upon conviction thereof, be punished by a fine of not less than ten thousand pesos (P,= 10,000.00) and suffer imprisonment of not less than one (1) year but not more than ten (10) years.</p>
							<p>Every officer or employee of the Government of the Republic of the Philippines or any of its agencies and instrumentalities, its political subdivisions, as well as government-owned or controlled corporations, including the Bangko Sentral ng Pilipinas, who, under the provisions of the NIRC, as amended, or regulations promulgated thereunder, is charged with the duty to deduct and withhold any internal revenue tax and to remit the same in accordance with the provisions of the NIRC, as amended, and other laws is guilty of any offense herein below specified shall upon conviction of each act or omission, be fined in a sum not less than five thousand pesos (P,= 5,000)  but not  more  than  fifty  thousand   pesos (P,= 50,000) or imprisoned for a term of not less than six (6) months and one day but not more than two (2) years, or both:</p>
							<ol class="indent" type="a">
							    <li>Those who fail or cause the failure to deduct and withhold any internal revenue tax under any of the withholding tax laws and implementing rules and regulations;</li>
							    <li>Those who fail or cause the failure to remit taxes deducted and withheld within the time prescribed by law, and implementing rules and regulations; and</li>
							    <li>Those who fail or cause the failure to file a form or statement within the time prescribed, or render or furnish a false or fraudulent form or statement required under the withholding tax laws and rules and regulations.</li>
							</ol>
							<p>If the withholding agent is the Government or any of its agencies, political subdivisions or instrumentalities, or a government-owned or controlled corporation, the employee thereof responsible for the withholding and remittance of tax shall be personally liable for the additions to the tax prescribed by the NIRC, as amended.</p>
							                      
							<dt>Note: All background information must be properly filled out.</dt>
							<ul class="indent">
							    <li>Box No. 1 refers to transaction period and not the date of filing this form.</li>
							    <li>The last 5 digits of the 14-digit TIN refer to the branch code.</li>
							    <li>All forms filed by an accredited tax agent on behalf of a taxpayer shall bear the following information:</li>
							        <ol type="A" class="indent">
							            <li>For Individual (CPAs, members of GPPs, and others)</li>
							            <dd>a.1 Taxpayer Identification Number (TIN); and </dd>
							            <dd>a.2 BIR Accreditation Number, Date of Issue, and Date of Expiry.</dd>
							            <li>For members of the Philippine Bar (Lawyers)</li>
							            <dd>b.1 Taxpayer Identification Number (TIN); </dd>
							            <dd>b.2 Attorney’s Roll Number;</dd>
							            <dd>b.3 Mandatory Continuing Legal Education (MCLE) Compliance Number; and</dd>
							            <dd>b.4 BIR Accreditation Number, Date of Issue, and Date of Expiry.</dd>
